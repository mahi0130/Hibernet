package in.co.rays.oneone;

public class Employee {
	private int id;
	private String name;
	private Address emaddress;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Address getEmaddress() {
		return emaddress;
	}
	public void setEmaddress(Address emaddress) {
		this.emaddress = emaddress;
	}

}
